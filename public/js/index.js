
const menuBtn = document.getElementById("menuBtn");
const menu = document.getElementById("menu");

const btnLeft = document.getElementById("btnLeft");
const btnRigth = document.getElementById("btnRigth");
const imgContainer = document.getElementById("imgContainer");
const IMAGENES = [
    './images/monitor.png',
    './images/monitor2.png',
    './images/monitor3.png'
]

let index = 0
menuBtn.addEventListener('click', () => {
    menu.classList.toggle('showMenu')
})

function changeImageNovedades(index,cb) {
    cb(index)
}

btnLeft.addEventListener('click', () => {
    console.log(`Izquierda. Index es: ${index}`)
    changeImageNovedades(index,changeLeft)
})

btnRigth.addEventListener('click', () => {
    console.log(`Derecha. Index es: ${index}`)
    changeImageNovedades(index,changeRigth)
})

function changeLeft(index) {
    if(index > 0){
        imgContainer.setAttribute('src',IMAGENES[index-1])
        index--
    }
}

function changeRigth(index) {
    if(index < IMAGENES.length - 1){
        imgContainer.setAttribute('src',IMAGENES[index+1])
        index++
    }
}